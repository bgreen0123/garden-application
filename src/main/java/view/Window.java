package view;

public class Window {
		int height;
		int width;
		
		public void draw(){}
		
		//Getters
		public int getHeight() {
			return height;
		}
		
		public int getWidth() {
			return width;
		}
		
		//Setters
		public void setHeight(int h) {
			height = h;
		}
		
		public void setWidth(int w) {
			width = w;
		}
}
