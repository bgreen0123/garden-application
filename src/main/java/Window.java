
public class Window {
		int height;
		int width;
		Garden garden;
		
		public int getHeight() {
			return height;
		}
		
		public void setHeight(int h) {
			height = h;
		}
		
		public int getWidth() {
			return width;
		}
		
		public void setWidth(int w) {
			width = w;
		}
		
		public Garden getGarden() {
			return garden;
		}
		
		public void setGarden(Garden g) {
			garden = g;
		}
}
